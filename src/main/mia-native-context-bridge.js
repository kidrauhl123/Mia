"use strict";

const fs = require("node:fs");
const path = require("node:path");

const CONTEXT_FILE_NAMES = Object.freeze({
  identity: "IDENTITY.md",
  tools: "TOOLS.md"
});

function cleanText(value = "") {
  return String(value || "")
    .replace(/\r/g, "")
    .trim();
}

function cleanLine(value = "") {
  return cleanText(value).replace(/\s+/g, " ").trim();
}

function atomicWriteFile(fsImpl, filePath, content) {
  fsImpl.mkdirSync(path.dirname(filePath), { recursive: true });
  let existing = "";
  try {
    existing = fsImpl.readFileSync(filePath, "utf8");
  } catch {
    existing = "";
  }
  if (existing === content) return false;
  const tempPath = `${filePath}.tmp-${process.pid}-${Date.now()}`;
  fsImpl.writeFileSync(tempPath, content, { encoding: "utf8", mode: 0o600 });
  fsImpl.renameSync(tempPath, filePath);
  return true;
}

function renderNativeIdentityMd({ botId = "mia", botName = "", personaText = "" } = {}) {
  const id = cleanLine(botId) || "mia";
  const name = cleanLine(botName) || id;
  const persona = cleanText(personaText);
  const lines = [
    "# Mia Bot Identity",
    "",
    "<!-- Generated by Mia from the current bot profile. Edit the bot in Mia, not this file. -->",
    `Bot: ${name}`,
    `Bot ID: ${id}`,
    "",
    "## Persona",
    "",
    persona || "No custom persona is configured for this Mia bot."
  ];
  return lines.join("\n").trimEnd() + "\n";
}

function renderNativeToolsMd({ botId = "mia", sessionId = "", skillMaterialization = null } = {}) {
  const id = cleanLine(botId) || "mia";
  const session = cleanLine(sessionId);
  const indexBlock = cleanText(skillMaterialization?.indexBlock || "");
  const loadedIds = Array.isArray(skillMaterialization?.loadedSkillIds)
    ? skillMaterialization.loadedSkillIds.map(cleanLine).filter(Boolean)
    : [];
  const lines = [
    "# Mia Tools And Skills",
    "",
    "<!-- Generated by Mia for this bot workspace. Edit skills in Mia, not this file. -->",
    `Bot ID: ${id}`,
    ...(session ? [`Session: ${session}`] : []),
    "",
    "## Mia App Tools",
    "",
    "- Use `memory_search`, `memory_remember`, `memory_update`, and `memory_forget` through the `mia-app` MCP server for Mia-owned scoped memory.",
    "- Use `skill_list_current` and `skill_read_current` through the `mia-app` MCP server to inspect full skill guides only when useful.",
    "- Do not copy memory or skill guide text into replies unless it is directly relevant to the user's request.",
    ""
  ];
  if (loadedIds.length) {
    lines.push("## Prioritized Skills", "");
    for (const skillId of loadedIds) lines.push(`- ${skillId}`);
    lines.push("");
  }
  if (indexBlock) {
    lines.push("## Enabled Skill Index", "", indexBlock, "");
  }
  return lines.join("\n").trimEnd() + "\n";
}

function syncNativeContextFiles(input = {}) {
  const fsImpl = input.fs || fs;
  const workspaceDir = cleanText(input.workspaceDir);
  if (!workspaceDir) throw new Error("workspaceDir is required for native context file sync.");
  const bot = input.bot && typeof input.bot === "object" ? input.bot : {};
  const botId = cleanLine(input.botId || bot.key || bot.id || "mia") || "mia";
  const botName = cleanLine(input.botName || bot.name || botId) || botId;
  const personaText = cleanText(input.personaText || "");
  const files = [];

  if (input.includePersona !== false) {
    const filePath = path.join(workspaceDir, CONTEXT_FILE_NAMES.identity);
    const content = renderNativeIdentityMd({ botId, botName, personaText });
    files.push({
      name: CONTEXT_FILE_NAMES.identity,
      path: filePath,
      changed: atomicWriteFile(fsImpl, filePath, content),
      chars: content.length
    });
  }

  if (input.includeSkills !== false) {
    const filePath = path.join(workspaceDir, CONTEXT_FILE_NAMES.tools);
    const content = renderNativeToolsMd({
      botId,
      sessionId: input.sessionId,
      skillMaterialization: input.skillMaterialization
    });
    files.push({
      name: CONTEXT_FILE_NAMES.tools,
      path: filePath,
      changed: atomicWriteFile(fsImpl, filePath, content),
      chars: content.length
    });
  }

  return {
    ok: true,
    engine: cleanLine(input.engine || "agent") || "agent",
    botId,
    sessionId: cleanLine(input.sessionId || ""),
    workspaceDir,
    files,
    count: files.length,
    changed: files.some((file) => file.changed)
  };
}

module.exports = {
  CONTEXT_FILE_NAMES,
  renderNativeIdentityMd,
  renderNativeToolsMd,
  syncNativeContextFiles
};
