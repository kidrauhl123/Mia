"use strict";

const crypto = require("node:crypto");
const fs = require("node:fs");
const path = require("node:path");

function cleanText(value = "") {
  return String(value || "")
    .replace(/\r/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function safeSegment(value = "", fallback = "default") {
  const text = String(value || "").trim().toLowerCase();
  const safe = text
    .replace(/[^a-z0-9._-]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .slice(0, 80);
  return safe || fallback;
}

function memoryFingerprint(text = "") {
  return crypto.createHash("sha1")
    .update(cleanText(text).toLowerCase().replace(/[^\p{L}\p{N}\s]/gu, " ").replace(/\s+/g, " ").trim())
    .digest("hex");
}

function nativeMemoryWorkspace({ runtimePaths, engine = "agent", botId = "mia" } = {}) {
  if (typeof runtimePaths !== "function") throw new Error("runtimePaths dependency is required.");
  const p = runtimePaths();
  if (!p?.home) throw new Error("runtimePaths().home is required.");
  return path.join(p.home, "native-memory", safeSegment(engine, "agent"), safeSegment(botId, "mia"));
}

function nativeMemoryFilePath(input = {}) {
  const explicit = String(input.workspaceDir || "").trim();
  const workspace = explicit || nativeMemoryWorkspace(input);
  return path.join(workspace, "MEMORY.md");
}

function groupEntries(entries = []) {
  const groups = { user: [], bot: [], session: [] };
  const seen = new Set();
  for (const entry of Array.isArray(entries) ? entries : []) {
    const text = cleanText(entry?.text || entry);
    if (!text) continue;
    const fingerprint = memoryFingerprint(text);
    if (seen.has(fingerprint)) continue;
    seen.add(fingerprint);
    const scope = ["user", "bot", "session"].includes(entry?.scope) ? entry.scope : "bot";
    groups[scope].push({ ...entry, text, fingerprint });
  }
  return groups;
}

function renderNativeMemoryMd({ entries = [], botId = "mia", sessionId = "" } = {}) {
  const groups = groupEntries(entries);
  const lines = [
    "# Mia Memories",
    "",
    "<!-- Generated by Mia from policy-approved scoped memories. Edit memories in Mia, not this file. -->",
    `Bot: ${cleanText(botId) || "mia"}`,
    ...(sessionId ? [`Session: ${cleanText(sessionId)}`] : []),
    ""
  ];
  const sections = [
    ["User Memory", groups.user],
    ["Bot Memory", groups.bot],
    ["Session Memory", groups.session]
  ];
  for (const [title, list] of sections) {
    if (!list.length) continue;
    lines.push(`## ${title}`, "");
    for (const item of list) lines.push(`- ${item.text.replace(/\n+/g, " ")}`);
    lines.push("");
  }
  return lines.join("\n").trimEnd() + "\n";
}

function atomicWriteFile(fsImpl, filePath, content) {
  fsImpl.mkdirSync(path.dirname(filePath), { recursive: true });
  let existing = "";
  try {
    existing = fsImpl.readFileSync(filePath, "utf8");
  } catch {
    existing = "";
  }
  if (existing === content) return false;
  const tempPath = `${filePath}.tmp-${process.pid}-${Date.now()}`;
  fsImpl.writeFileSync(tempPath, content, { encoding: "utf8", mode: 0o600 });
  fsImpl.renameSync(tempPath, filePath);
  return true;
}

function collectScopedEntries({ memoryService, userId = "", botId = "mia", sessionId = "default", includeSession = true, limit = 120 } = {}) {
  if (!memoryService || typeof memoryService.listMemories !== "function") {
    throw new Error("memoryService.listMemories dependency is required.");
  }
  const scopes = includeSession ? ["user", "bot", "session"] : ["user", "bot"];
  const entries = [];
  const perScopeLimit = Math.max(1, Math.min(200, Math.floor(Number(limit) || 120)));
  for (const scope of scopes) {
    entries.push(...memoryService.listMemories({
      userId,
      botId,
      sessionId,
      scopes: [scope],
      status: "active",
      limit: perScopeLimit
    }));
  }
  return entries.slice(0, perScopeLimit);
}

function syncNativeMemoryFiles(input = {}) {
  const fsImpl = input.fs || fs;
  const engine = safeSegment(input.engine || "agent", "agent");
  const botId = cleanText(input.botId || "mia") || "mia";
  const sessionId = cleanText(input.sessionId || "default") || "default";
  const entries = Array.isArray(input.entries)
    ? input.entries
    : collectScopedEntries({
      memoryService: input.memoryService,
      userId: input.userId,
      botId,
      sessionId,
      includeSession: input.includeSession !== false,
      limit: input.limit
    });
  const memoryPath = nativeMemoryFilePath({ ...input, engine, botId });
  const content = renderNativeMemoryMd({ entries, botId, sessionId });
  const changed = atomicWriteFile(fsImpl, memoryPath, content);
  return {
    ok: true,
    engine,
    botId,
    sessionId,
    workspaceDir: path.dirname(memoryPath),
    memoryPath,
    count: groupEntries(entries).user.length + groupEntries(entries).bot.length + groupEntries(entries).session.length,
    changed
  };
}

module.exports = {
  nativeMemoryFilePath,
  nativeMemoryWorkspace,
  renderNativeMemoryMd,
  safeSegment,
  syncNativeMemoryFiles
};
